class Endereco{
    constructor(cep, rua, numero, complemento, bairro, cidade, estado, pais){
        this.cep = cep
        this.rua = rua
        this.numero = numero
        this.complemento = complemento
        this.bairro = bairro
        this.cidade = cidade
        this.estado = estado
        this.pais = pais
    }
}